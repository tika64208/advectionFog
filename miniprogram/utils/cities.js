/**
 * 从内置数据库查找城市
 * @param {string} query 搜索关键词
 * @returns {object|null} 城市信息或 null
 */
export function findCity(query) {
  const q = query.trim()
  
  // 精确匹配
  if (CITIES[q]) {
    return { ...CITIES[q] }
  }
  
  // 模糊匹配（去掉"市"后缀）
  const qNoSuffix = q.replace(/市$/, '')
  if (CITIES[qNoSuffix]) {
    return { ...CITIES[qNoSuffix] }
  }
  
  // 部分匹配
  for (const [name, data] of Object.entries(CITIES)) {
    if (name.includes(q) || q.includes(name)) {
      return { ...data }
    }
  }
  
  return null
}
